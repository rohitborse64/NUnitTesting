namespace Sparky
{
    public class Calculator
    {
        public int AddNumbers(int num1, int num2)
        {
            return num1 + num2;
        }

        public double AddNumbersDouble(double num1, double num2) 
        {  
            return num1 + num2; 
        }

        public bool isOddNumber(int num)
        {
            return num % 2 != 0;
        }





    }
}